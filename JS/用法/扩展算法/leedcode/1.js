/**
 * ========================================
 * LeetCode 1. 两数之和 (Two Sum)
 * ========================================
 * - 外层循环从索 引0开始         
 * - 内层循环从i+1开 始，避免重复比较     
 * - 找到两数之和等于target时，记录下标并返回
 * 
 * 【代码分析】
 * ✅ 优点：
 *    - 思路直观，容易理解
 *    - 代码结构清晰
 * ❌ 缺点：
 *    - 时间复杂度O(n²)，效率较低
  - newArray命名不够语义化
 *    - break只能跳出内层循环，但逻辑上没问题
 * 
 * 【优化思路】
 * 使用哈希表（Map）将查找时间从O(n)降到O(1)：
 * 1. 创建一个Map存储 {需要的补数: 当前下标}
 * 2. 遍历数组，对于每个元素nums[i]：
 *    - 如果nums[i]在Map中，说明找到了配对，返回[map.get(nums[i]), i]
 *    - 否则，将(target - nums[i])存入Map
 * 
 * 【复杂度对比】
 * 当前：时间O(n²) 空间O(1)
 * 优化：时间O(n)   空间O(n)
 **/

// map 解决
var twoSum = function (nums, target) {
    let map = new Map();
    for (let i = 0; i < nums.length; i++) {
        if (map.has(nums[i])) {
            return [map.get(nums[i]), i];
        }
        map.set(target - nums[i], i);
    }
    return [];
}

console.log(twoSum([2, 7, 11, 15, 1], 9))



// //优化思路 ✅
// 用哈希表把"查找另一个数"从 O(n) 降到 O(1)
// 优化写法（思路）
// 遍历数组
// 用 Map存：目标值 - 当前值 → 下标
// 如果当前值在 Map 里，直接返回
// 复杂度
// 时间：O(n)
// 空间：O(n)
