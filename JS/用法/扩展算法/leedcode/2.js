/**
 * ========================================
 * LeetCode 2. 两数相加 (Add Two Numbers)
 * ========================================
 * 
 * 【评分】⭐⭐ (2/5)
 * 
 * 【你的思路】
 * 将链表当作数组处理：
 * 1. 从后往前遍历数组，用Math.pow(10, i)计算位权
 * 2. 将两个数组转换成整数后相加
 * 3. 将结果转回数组格式
 * 
 * 【代码分析】
 * ✅ 优点：
 *    - 思路有一定逻辑性
 *    - 能处理简单的测试用例
 * ❌ 缺点：
 *    - 题目要求是链表，但代码把链表当数组用（审题错误）
 *    - Math.pow计算大数时会丢失精度（JS数值精度问题）
 *    - 无法处理超过Number.MAX_SAFE_INTEGER的大数
 *    - 反转逻辑复杂，容易出错
 * 
 * 【优化思路】
 * 模拟人工加法，逐位相加 + 进位：
 * 1. 同时遍历两个链表，逐位相加
 * 2. 用carry变量记录进位
 * 3. 构建新链表存储结果
 * 
 * 【关键点】
 * - 不需要转换成整数，直接逐位处理
 * - 处理链表长度不等的情况
 * - 最后如果carry>0，需要额外添加节点
 * 
 * 【复杂度】
 * 时间：O(max(m, n)) - m、n为两个链表长度
 * 空间：O(max(m, n)) - 结果链表长度
 * 
 * 【面试建议】
 * 链表题的核心是"指针操作"和"边界处理"，
 * 不要试图转换成其他数据结构来绕过。
 * ========================================
 */

l1 = [1, 2, 3];
l2 = [4, 5, 6];

let num1 = 0;
let num2 = 0;
let num3 = 0;
let l3 = [];
for (let i = l1.length - 1; i >= 0; i--) {
    num1 += l1[i] * (Math.pow(10, i));
}
for (let i = l2.length - 1; i >= 0; i--) {
    num2 += l2[i] * (Math.pow(10, i));
}
num3 = num1 + num2;

num3 = Array.from(num3.toString());
console.log(num3);
for (let i = 0; i < num3.length; i++) {
    l3[num3.length - 1 - i] = Number(num3[i]);
}
console.log(l3);
